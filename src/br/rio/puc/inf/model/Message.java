package br.rio.puc.inf.model;

public class Message {

	private Integer ID;
	
	private String Message;

	// Public Constructor
	public Message() {
		
	}
	
	
	// Getters & Setters
	
	public Integer getID() {
		return ID;
	}

	public void setID(Integer iD) {
		ID = iD;
	}

	public String getMessage() {
		return Message;
	}

	public void setMessage(String message) {
		Message = message;
	}
	
	
	
}
