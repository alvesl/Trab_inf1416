package br.rio.puc.inf.model;

public class Group {

	private Integer groupID;
	
	private String name;

	
	// Public Constructor
	public Group() {
		
	}
	
	// Getters & Setters
	
	public Integer getGroupID() {
		return groupID;
	}

	public void setGroupID(Integer groupID) {
		this.groupID = groupID;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
